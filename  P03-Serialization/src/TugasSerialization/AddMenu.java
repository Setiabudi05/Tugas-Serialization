    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String txt_item_name = txtNamaItem.getText();
        String txt_item_price = txtHargaItem.getText();
        Object[] item = {txt_item_name, txt_item_price};
        model.addRow(item);

        jComboBox1.addItemListener(new ItemListener() {
            public void itemStateChanged(ItemEvent e) {
                if (e.getItem().toString().equals("MINUMAN")) {
                    String nama_produk = JOptionPane.showInputDialog("Masukkan Nama Produk:");
                    String nama_item = JOptionPane.showInputDialog("Masukkan Nama Item:");
                    String harga_item = JOptionPane.showInputDialog("Masukkan Harga Item:");

                    DefaultTableModel model = (DefaultTableModel) tabelItem.getModel();
                    model.addRow(new Object[]{nama_item, null, harga_item, null});
                }
            }
        });
    }//GEN-LAST:event_btnTambahActionPerformed

