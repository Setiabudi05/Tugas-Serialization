    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        String selectedOption = (String) jComboBox1.getSelectedItem();
        if (selectedOption.equals("MINUMAN") || selectedOption.equals("MAKANAN")) {
            // Menetapkan nilai ID Produk dari JTextField txtId
            String id_produk = JOptionPane.showInputDialog("Masukkan ID Produk:");

            // Menetapkan nilai Nama Produk dari JTextField txtNamaProduk
            String nama_produk = JOptionPane.showInputDialog("Masukkan Nama Produk:");

            String nama_item = JOptionPane.showInputDialog("Masukkan Nama Item:");

            // Menampilkan dialog untuk memasukkan Harga Item
            String harga_item = JOptionPane.showInputDialog("Masukkan Harga Item:");

            // Menambahkan data ke tabel
            DefaultTableModel model = (DefaultTableModel) tabelItem.getModel();
            if (selectedOption.equals("MINUMAN")) {
                model.addRow(new Object[]{null, nama_item, harga_item});
            } else {
                model.addRow(new Object[]{nama_item, null, harga_item});
            }

            // Menetapkan nilai ID Produk ke dalam JTextField txtId
            txtId.setText(id_produk);

            // Menetapkan nilai Nama Produk ke dalam JTextField txtNamaProduk
            txtNamaProduk.setText(nama_produk);

            // Menetapkan nilai Nama Item ke dalam JTextField txtNamaItem
            txtNamaItem.setText(nama_item);

            // Menetapkan nilai Harga Item ke dalam JTextField txtHargaItem
            txtHargaItem.setText(harga_item);
        }

    }//GEN-LAST:event_jComboBox1ActionPerformed

